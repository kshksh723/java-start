package variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32768 ~ 32767
        int i = 2147483647; //-2,147,483,648 ~ 2,147,483,647 (약 20억)


        long l = 9223372036854775807L;

        // 실수
        float f =  10.0f;
        double d = 10.0;

        // 정수의 리터럴은 int로 기본으로 사용한다
    }
}
